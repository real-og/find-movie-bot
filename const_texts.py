def compose_saved(films: Union[Movie, Serial, None]) -> str:
    if (films == None) or len(films) == 0:
        return '–ø—É—Å—Ç–æ'
    text = '–¢–≤–æ–∏:\n'
    for film in films:
        text += f"{film.title} {film.actors}  {film.has_oscar}\n"
    return text

